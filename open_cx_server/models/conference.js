const mongoose = require('mongoose');

const conferenceSchema = mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  location: {
    type: String,
    required: true
  },
  startDate: {
    type: Date,
    required: true
  },
  endDate: {
    type: Date,
    required: true
  },
  image: String
});

module.exports = conference = mongoose.model('Conference', conferenceSchema);
